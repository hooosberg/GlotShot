Object.entries(projectSourceTranslations).forEach(([lang, bundle]) => {
    if (translations[lang]) {
        translations[lang] = {
            ...translations[lang],
            menu: {
                ...(translations[lang].menu || {}),
                ...(bundle.menu || {}),
            },
            rightPanel: {
                ...(translations[lang].rightPanel || {}),
                ...(bundle.rightPanel || {}),
            },
            alerts: {
                ...(translations[lang].alerts || {}),
                ...(bundle.alerts || {}),
            },
            project: {
                ...(translations[lang].project || {}),
                ...(bundle.project || {}),
            },
        };
    }
});

// 为未翻译的语言提供回退
Object.keys(translations).forEach(lang => {
    if (translations[lang] === null) {
        // 对于繁体中文，回退到简体中文；其他语言回退到英文
        translations[lang] = lang === 'zh-TW' ? translations['zh-CN'] : translations['en'];
    }
});
